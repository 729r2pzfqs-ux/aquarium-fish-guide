// Tank size conversion (gallons to liters, metric-first)
function tankSizeES(gallons) {
    const liters = Math.round(gallons * 3.785);
    return `${liters}L (${gallons}gal)`;
}

// Size conversion (inches to cm, metric-first)
function sizeES(inches) {
    const cm = Math.round(inches * 2.54);
    return `${cm}cm (${inches}")`;
}

// Temperature conversion (F to C, metric-first)
function tempRangeES(minF, maxF) {
    const minC = Math.round((minF - 32) * 5/9);
    const maxC = Math.round((maxF - 32) * 5/9);
    return `${minC}-${maxC}°C (${minF}-${maxF}°F)`;
}

